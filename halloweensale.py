# Complete the howManyGames function below.
def howManyGames(p, d, m, s):
    c=p
    l=[]
    while(sum(l)<=s):
        if c>m:
            l.append(c)
            c=c-d
        if c<=m:
            l.append(m)
    print(l)
    return (len(l)-1)



